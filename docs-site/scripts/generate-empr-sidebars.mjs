/**
 * Generate Docusaurus sidebar entries for @empr/* packages from copied docs on disk.
 *
 * Usage:
 *   node scripts/generate-empr-sidebars.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildPackageSidebarItems, scanDocsPackageDir } from './lib/empr-sidebar.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DOCS_ROOT = path.resolve(__dirname, '../docs');
const OUTPUT_PATH = path.resolve(__dirname, '../sidebars.empr.generated.ts');

/** @type {Array<{ id: string; destSubdir: string; label: string; docIdPrefix: string; indexId: string }>} */
const FEATURE_PACKAGES = [
  {
    id: 'es',
    destSubdir: '',
    label: '@empr/es',
    docIdPrefix: 'features',
    indexId: 'features/index',
  },
  {
    id: 'es-sistema',
    destSubdir: 'es-sistema',
    label: '@empr/es-sistema',
    docIdPrefix: 'features/es-sistema',
    indexId: 'features/es-sistema/index',
  },
  {
    id: 'es-componente',
    destSubdir: 'es-componente',
    label: '@empr/es-componente',
    docIdPrefix: 'features/es-componente',
    indexId: 'features/es-componente/index',
  },
  {
    id: 'es-lienzo',
    destSubdir: 'es-lienzo',
    label: '@empr/es-lienzo',
    docIdPrefix: 'features/es-lienzo',
    indexId: 'features/es-lienzo/index',
  },
];

/** @type {Array<{ id: string; label: string }>} */
const API_PACKAGES = [
  { id: 'es', label: '@empr/es' },
  { id: 'es-sistema', label: '@empr/es-sistema' },
  { id: 'es-componente', label: '@empr/es-componente' },
  { id: 'es-lienzo', label: '@empr/es-lienzo' },
];

/**
 * @description Builds top-level package category for features or API sidebars.
 * @param {string} label Navbar category label.
 * @param {string} indexId Docusaurus doc id for package index.
 * @param {string} docIdPrefix Doc id prefix for nested modules.
 * @param {Record<string, string[]>} layerFeatures Layer scan map from disk.
 * @returns {import('@docusaurus/plugin-content-docs').SidebarsConfig[string][number]} Sidebar category node.
 */
function buildPackageCategory(label, indexId, docIdPrefix, layerFeatures) {
  return {
    type: 'category',
    label,
    link: { type: 'doc', id: indexId },
    collapsed: false,
    items: buildPackageSidebarItems(docIdPrefix, layerFeatures),
  };
}

const emprFeaturesSidebar = FEATURE_PACKAGES.map((pkg) => {
  const packageDir = pkg.destSubdir
    ? path.join(DOCS_ROOT, 'features', pkg.destSubdir)
    : path.join(DOCS_ROOT, 'features');
  const layerFeatures = scanDocsPackageDir(packageDir);
  return buildPackageCategory(pkg.label, pkg.indexId, pkg.docIdPrefix, layerFeatures);
});

const emprApiSidebar = API_PACKAGES.map((pkg) => {
  const packageDir = path.join(DOCS_ROOT, 'api', pkg.id);
  const layerFeatures = scanDocsPackageDir(packageDir);
  return buildPackageCategory(pkg.label, `api/${pkg.id}/index`, `api/${pkg.id}`, layerFeatures);
});

const output = `// AUTO-GENERATED by scripts/generate-empr-sidebars.mjs — do not edit.

export const emprFeaturesSidebar = ${JSON.stringify(emprFeaturesSidebar, null, 2)};

export const emprApiSidebar = ${JSON.stringify(emprApiSidebar, null, 2)};
`;

fs.writeFileSync(OUTPUT_PATH, output, 'utf8');
console.log(`Wrote ${OUTPUT_PATH}`);
